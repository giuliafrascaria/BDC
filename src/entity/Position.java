package entity;

public class Position {
	private String raH;
	private String raM;
	private String raS;
	private boolean deSgn;//true if +, false if -
	private String deD;
	private String deM;
	private String deS;
	private String redShift;
	private String galaxy;
	
	public String getRaH() {
		return raH;
	}
	
	public void setRaH(String raH) {
		this.raH = raH;
	}
	
	public String getRaM() {
		return raM;
	}
	
	public void setRaM(String raM) {
		this.raM = raM;
	}
	
	public String getRaS() {
		return raS;
	}
	
	public void setRaS(String raS) {
		this.raS = raS;
	}
	
	public boolean getDeSgn() {
		return deSgn;
	}
	
	public void setDeSgn(boolean desgn) {
		this.deSgn = desgn;
	}
	
	public String getDeD() {
		return deD;
	}
	
	public void setDeD(String deD) {
		this.deD = deD;
	}
	
	public String getDeM() {
		return deM;
	}
	
	public void setDeM(String deM) {
		this.deM = deM;
	}
	
	public String getDeS() {
		return deS;
	}
	
	public void setDeS(String deS) {
		this.deS = deS;
	}
	
	public String getRedShift() {
		return redShift;
	}
	
	public void setRedShift(String redShift) {
		this.redShift = redShift;
	}

	public String getGalaxy() {
		return galaxy;
	}

	public void setGalaxy(String galaxy) {
		this.galaxy = galaxy;
	}
	
	
}
